package chapter2;

public class DoubleEx1 {

	public static void main(String[] args) {
		// 실수형 데이터 타입 : float(4), double(8)
		// 실수형 데이터는 부동소수점 방식
		
		float fnum = 3.12F; // float 데이터 표현은 마지막에 알파벳 F or f로 표현(규칙)
		double dnum = 3.14; // 3.14D 동일한 표현인데 D가 생략되어 있다.
		
		System.out.println(fnum);
		System.out.println(dnum);

		
	}

}
